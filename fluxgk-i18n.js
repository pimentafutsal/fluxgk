  var STORE='fluxgk_lang';
  var _lang=null, _subs=[], _applying=false, _timer=null;
  var SEL='[data-i18n],[data-i18n-placeholder],[data-i18n-title],[data-i18n-aria],[data-i18n-alt],[data-i18n-value]';

  function detect(){
    try{
      var s=localStorage.getItem(STORE);
      if(s==='en'||s==='pt')return s;
    }catch(e){}
    var n='';
    try{ n=(navigator.language||navigator.userLanguage||'en').toLowerCase(); }catch(e){}
    return n.indexOf('pt')===0 ? 'pt' : 'en';
  }

  function lookup(lang,key){
    var d=DICT[lang];
    if(d && d[key]!=null) return d[key];
    if(DICT.en[key]!=null) return DICT.en[key];   /* fallback: EN */
    return null;
  }

  var I18N={
    /* idioma atual (detecta na primeira chamada) */
    getLang:function(){ return _lang||(_lang=detect()); },

    /* persist=false quando o idioma vem do hub (nao sobrescreve a escolha local) */
    setLang:function(l,persist){
      if(l!=='en'&&l!=='pt') return;
      var changed = (_lang!==l);
      _lang=l;
      if(persist!==false){ try{ localStorage.setItem(STORE,l); }catch(e){} }
      try{ document.documentElement.setAttribute('lang', l==='pt'?'pt-BR':'en'); }catch(e){}
      I18N.apply();
      if(changed) _subs.forEach(function(fn){ try{ fn(l); }catch(e){} });
    },

    /* traducao pontual */
    t:function(key,fallback){
      var v=lookup(I18N.getLang(),key);
      if(v!=null) return v;
      return fallback!=null ? fallback : key;
    },

    /* avisa quando o idioma muda (para re-renderizar telas dinamicas) */
    onChange:function(fn){ if(typeof fn==='function') _subs.push(fn); },

    /* aplica nas marcacoes data-i18n* */
    apply:function(root){
      root=root||document;
      if(!root.querySelectorAll) return;
      _applying=true;
      try{ I18N._apply(root); } finally { _applying=false; }
    },

    _apply:function(root){
      var ATTR=[['data-i18n-placeholder','placeholder'],
                ['data-i18n-title','title'],
                ['data-i18n-aria','aria-label'],
                ['data-i18n-alt','alt'],
                ['data-i18n-value','value']];
      Array.prototype.forEach.call(root.querySelectorAll('[data-i18n]'),function(el){
        var v=lookup(I18N.getLang(),el.getAttribute('data-i18n'));
        if(v!=null){
          if(el.hasAttribute('data-i18n-html')) el.innerHTML=v; else el.textContent=v;
        }
      });
      ATTR.forEach(function(p){
        Array.prototype.forEach.call(root.querySelectorAll('['+p[0]+']'),function(el){
          var v=lookup(I18N.getLang(),el.getAttribute(p[0]));
          if(v!=null) el.setAttribute(p[1],v);
        });
      });
    },

    /* util para diagnostico: chaves faltando no PT */
    missing:function(){
      return Object.keys(DICT.en).filter(function(k){ return DICT.pt[k]==null; });
    },

    _dict:DICT
  };

  /* ── OBSERVADOR ────────────────────────────────────────────
     Varias telas recriam HTML via innerHTML depois que a
     traducao ja foi aplicada, trazendo de volta o texto padrao.
     Aqui re-aplicamos sempre que markup marcado entra no DOM.  */
  function watch(){
    if(!global.MutationObserver||!document.body) return;
    new MutationObserver(function(muts){
      if(_applying) return;                    /* evita realimentacao */
      var need=false;
      for(var i=0;i<muts.length&&!need;i++){
        var add=muts[i].addedNodes;
        for(var j=0;j<add.length;j++){
          var n=add[j];
          if(!n||n.nodeType!==1) continue;     /* ignora nos de texto */
          if((n.hasAttribute&&n.hasAttribute('data-i18n'))||
             (n.querySelector&&n.querySelector(SEL))){ need=true; break; }
        }
      }
      if(!need) return;
      clearTimeout(_timer);
      _timer=setTimeout(function(){ I18N.apply(document); },0);
    }).observe(document.body,{childList:true,subtree:true});
  }

  global.FluxGKI18n=I18N;
  global.TX=function(k,f){ return I18N.t(k,f); };

  /* aplica assim que o DOM estiver pronto */
  if(document.readyState==='loading'){
    document.addEventListener('DOMContentLoaded',function(){ I18N.apply(); watch(); });
  } else { I18N.apply(); watch(); }

